namespace StrixMusic.Sdk.ViewModels
{
    /// <summary>
    /// The direction to sort in by a type.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// Indicates items should be taken in their default order.
        /// </summary>
        Unsorted,

        /// <summary>
        /// Items should be sorted in an Ascending order.
        /// </summary>
        Ascending,

        /// <summary>
        /// Items should be sorted in a Descending order.
        /// </summary>
        Descending,
    }
}
